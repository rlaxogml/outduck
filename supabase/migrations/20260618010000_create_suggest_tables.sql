  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
  user_id uuid REFERENCES auth.users(id) ON DELETE SET NULL,
  status text DEFAULT 'pending' NOT NULL CHECK (status IN ('pending', 'approved', 'rejected')),
  
  -- Common Fields
  is_offline boolean NOT NULL,
  is_online boolean NOT NULL,
  title text NOT NULL,
  description text,
  image_url text,
  links jsonb,
  
  -- Offline Specific Fields
  start_date date,
  end_date date,
  start_time time without time zone,
  end_time time without time zone,
  reservation_type text,
  is_reservation_always boolean DEFAULT false,
  reservation_starts_at timestamp with time zone,
  reservation_ends_at timestamp with time zone,
  locations jsonb,
  schedules jsonb,
  support_images jsonb,
  
  -- Online Specific Fields
  online_start_at timestamp with time zone,
  online_end_at timestamp with time zone,
  
  -- Mapping / Relation tracking
  channel_ids jsonb,
  channel_proposal_id bigint REFERENCES public.channel_proposals(id) ON DELETE SET NULL,
  approved_event_id bigint REFERENCES public.events(id) ON DELETE SET NULL
