-- ==========================================
-- 3. Trigger: Auto-reject event proposals on channel rejection
-- ==========================================
CREATE OR REPLACE FUNCTION public.handle_channel_proposal_rejection()
RETURNS TRIGGER AS $$
BEGIN
  IF NEW.status = 'rejected' AND OLD.status = 'pending' THEN
    UPDATE public.event_proposals
    SET status = 'rejected'
    WHERE channel_proposal_id = NEW.id AND status = 'pending';
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE TRIGGER trg_channel_proposal_rejection
  AFTER UPDATE ON public.channel_proposals
  FOR EACH ROW
  EXECUTE FUNCTION public.handle_channel_proposal_rejection();


-- ==========================================
-- 4. Trigger: Pre-approval check (Enforce channel approval first)
-- ==========================================
CREATE OR REPLACE FUNCTION public.check_event_proposal_approval()
RETURNS TRIGGER AS $$
DECLARE
  v_channel_status text;
BEGIN
  IF NEW.status = 'approved' AND (OLD.status IS NULL OR OLD.status <> 'approved') THEN
    IF NEW.channel_proposal_id IS NOT NULL THEN
      SELECT status INTO v_channel_status
      FROM public.channel_proposals
      WHERE id = NEW.channel_proposal_id;
      
      IF v_channel_status IS NULL OR v_channel_status <> 'approved' THEN
        RAISE EXCEPTION '연결된 채널 제안이 아직 승인되지 않았습니다. 채널 승인이 완료된 후 행사를 승인할 수 있습니다.';
      END IF;
    END IF;
  END IF;
  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE TRIGGER trg_check_event_proposal_approval
  BEFORE UPDATE ON public.event_proposals
  FOR EACH ROW
  EXECUTE FUNCTION public.check_event_proposal_approval();
