-- ==========================================
-- 1. Create channel_proposals Table
-- ==========================================
CREATE TABLE IF NOT EXISTS public.channel_proposals (
  id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  created_at timestamp with time zone DEFAULT timezone('utc'::text, now()) NOT NULL,
  user_id uuid REFERENCES auth.users(id) ON DELETE SET NULL,
  name text NOT NULL,
  type text NOT NULL,
  image_url text,
  links jsonb,
  status text DEFAULT 'pending' NOT NULL CHECK (status IN ('pending', 'approved', 'rejected')),
  approved_channel_id bigint REFERENCES public.channels(id) ON DELETE SET NULL
);

-- Enable RLS on channel_proposals
ALTER TABLE public.channel_proposals ENABLE ROW LEVEL SECURITY;

-- Select policy: Owner or Admin
CREATE POLICY "Enable select for owner or admin on channel_proposals" ON public.channel_proposals
  FOR SELECT USING (
    auth.uid() = user_id OR 
    EXISTS (
      SELECT 1 FROM public.profiles 
      WHERE id = auth.uid() AND is_admin = true
    )
  );

-- Insert policy: Authenticated users
CREATE POLICY "Enable insert for authenticated users on channel_proposals" ON public.channel_proposals
  FOR INSERT WITH CHECK (auth.uid() IS NOT NULL AND auth.uid() = user_id);

-- Update policy: Admin only
CREATE POLICY "Enable update for admins on channel_proposals" ON public.channel_proposals
  FOR UPDATE USING (
    EXISTS (
      SELECT 1 FROM public.profiles 
      WHERE id = auth.uid() AND is_admin = true
    )
  );

-- Delete policy: Admin only
CREATE POLICY "Enable delete for admins on channel_proposals" ON public.channel_proposals
  FOR DELETE USING (
    EXISTS (
      SELECT 1 FROM public.profiles 
      WHERE id = auth.uid() AND is_admin = true
    )
  );


-- ==========================================
-- 2. Create event_proposals Table
-- ==========================================
